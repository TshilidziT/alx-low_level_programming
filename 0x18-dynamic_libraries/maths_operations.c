#include <stdio.h>
/**add - adds numbers
 * @a: integer
 * @b: integer
 * Return: sum of a and b.
 */
int add(int a, int b)
{
	return (a + b);
}
/**sub - subtracts numbers
 * @a: integer
 * @b: integer
 * Return: difference
 */
int sub(int a, int b)
{
	return (a - b);
}
/**mul - multiplies numbers
 * @a: integer
 * @b: integer
 * Return: a*b
 */
int mul(int a, int b)
{
	return (a * b);
}
/**div - divides numbers
 * @a: integer
 * @b: integer
 * Return: a / b
 */
int div (int a, int b)
{
	return (a / b );
}
/**mod - modula
 * @a: integer
 * @b: integer
 * Return: a % b
 */
int mod (int a, int b)
{
	return (a % b);
}
